from termcolor import colored, cprint

def logo():
    l = """
                                            $V$         ###############/0/0/
            RuckusC0re - 2020              $V$      ##################/0/0/####
___________________   _______________     $V$       ###............../0/0/..###
\_________  \   _  \  \_____  \   _  \   $V$     ######.############/0/0/##.######
     /  ____/  /_\  \  /  ____/  /_\  \ $V$      ######.###########/0/0/###.######
    /       \  \_/   \/       \  \_/   \V$       ######.###       /0/0/####.######
    \_______ \_____  /\_______ \_____  /$        ######.###      /0/0/ ####.######
            \/     \/         \/     \/$         ######.###     /0/0/  ####.######
                                    $V$          ######.###    /0/0/   ####.######
    github.com/ruckusist/ruckuscore V$           ######.###   /0/0/    ####.######
                                  $V$            ######.###  /0/0/     ####.######
        *DataSmith               $V$             ######.### /0/0/      ####.######
        *Backend Server         $V$              ######.###/0/0/       ####.######
        *TUI/Curses interface  $V$               ######.##/0/0/############.######
                              $V$                ######.#/0/0/#############.######
                             $V$                    ###./0/0/...............###
                            $V$                     ###/0/0/###################
                           $V$                        /0/0/################
    """
    k = ""
    for i in l:
        if i == "*":
            i = colored("*", "magenta")
        if i == "0":
            i = colored("0", "magenta")
        if i == "_":
            i = colored("_", "white")
        if i == ".":
            i = colored(".", "white")
        if i == "\\":
            i = colored("\\", "red")
        if i == "#":
            i = colored("#", "red")
        if i == "V":
            i = colored("V", "blue")
        if i == "$":
            i = colored("$", "green")
        if i == "/":
            i = colored("/", "yellow")
        k += (i)
    print(k)

logo()